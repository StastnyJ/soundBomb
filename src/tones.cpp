char tonePrefixes[] = {'C', 'C', 'D', 'D', 'E', 'F', 'F', 'G', 'G', 'A', 'A', 'B'};
char toneSuffixes[] = {'\0', '#', '\0', '#', '\0', '\0', '#', '\0', '#', '\0', '#', '\0'};

double toneToFreq(int tone) {
  return tones[tone];
}

int freqToTone(double freq) {
  double bestDist = 999999;
  for (int res = 0; res < TONES; res++) {
    double dist = freq < tones[res] ? tones[res] - freq : freq - tones[res];
    if (dist < bestDist)
      bestDist = dist;
    else
      return res - 1;
  }
  return TONES - 1;
}

int toneToString(int tone, char *res) {
  return sprintf(res, "%c%d%c", tonePrefixes[tone % 12], tone / 12, toneSuffixes[tone % 12]);
}